function normalize(str) {
  return str.toLowerCase().replace(/[^a-z0-9çğıöşü ]/g, '').trim()
}

function matchProduct(itemName) {
  const normalized = normalize(itemName)
  const words = normalized.split(/\s+/)

  const entries = Object.entries(GLOBAL_MARKET_PRICES)
  let bestMatch = null
  let bestScore = 0

  for (const [productName] of entries) {
    const normalizedProduct = normalize(productName)
    const productWords = normalizedProduct.split(/\s+/)

    const allProductWordsPresent = productWords.every((pw) => words.includes(pw))

    if (!allProductWordsPresent) continue

    const score = productWords.length
    if (score > bestScore) {
      bestScore = score
      bestMatch = productName
    }
  }

  return bestMatch
}

function detectBrandHabits(inventoryItems) {
  const habits = {}
  for (const item of inventoryItems) {
    const productName = matchProduct(item.name)
    if (productName) {
      const brands = Object.keys(GLOBAL_MARKET_PRICES[productName].brands)
      for (const brand of brands) {
        if (normalize(item.name).includes(normalize(brand))) {
          habits[productName] = brand
          break
        }
      }
      if (!habits[productName]) {
        habits[productName] = brands[0]
      }
    }
  }
  return habits
}

function pickBrand(productName, strategy, habits) {
  const product = GLOBAL_MARKET_PRICES[productName]
  if (!product) return null
  const brandNames = Object.keys(product.brands)

  if (strategy === 'premium') {
    const preferred = habits[productName]
    if (preferred && product.brands[preferred]) return preferred
    const last = brandNames[brandNames.length - 1]
    const second = brandNames[brandNames.length - 2]
    return product.brands[last] ? last : second
  }

  if (strategy === 'fp') {
    const preferred = habits[productName]
    if (preferred && product.brands[preferred]) return preferred
    return brandNames[0]
  }

  const cheapest = brandNames.reduce((best, name) => {
    const prices = Object.values(product.brands[name])
    const avg = prices.reduce((a, b) => a + b, 0) / prices.length
    const bestAvg =
      Object.values(product.brands[best]).reduce((a, b) => a + b, 0) /
      prices.length
    return avg < bestAvg ? name : best
  }, brandNames[0])

  return cheapest
}

function findBestMarket(productName, brand, strategy, habits) {
  const product = GLOBAL_MARKET_PRICES[productName]
  if (!product || !product.brands[brand]) return null

  const prices = product.brands[brand]

  if (strategy === 'cheapest') {
    return Object.entries(prices).reduce((a, b) => (a[1] < b[1] ? a : b))[0]
  }

  if (strategy === 'premium') {
    return Object.entries(prices).reduce((a, b) => (a[1] > b[1] ? a : b))[0]
  }

  const preferred = habits[productName]
  if (preferred && product.brands[preferred]) {
    return Object.entries(product.brands[preferred]).reduce((a, b) =>
      a[1] < b[1] ? a : b,
    )[0]
  }

  return Object.entries(prices).reduce((a, b) => (a[1] < b[1] ? a : b))[0]
}

export async function fetchMarketPrices(
  shoppingList,
  strategy = 'cheapest',
  inventoryItems = [],
) {
  await new Promise((r) => setTimeout(r, 1200))

  const habits = detectBrandHabits(inventoryItems)

  const products = shoppingList
    .map((item) => {
      const productName = matchProduct(item.name)
      if (!productName) return null

      const brand = pickBrand(productName, strategy, habits)
      const bestMarket = findBestMarket(productName, brand, strategy, habits)
      const price =
        GLOBAL_MARKET_PRICES[productName]?.brands[brand]?.[bestMarket]

      if (!brand || !bestMarket || !price) return null

      return {
        originalName: item.name,
        productName,
        brand,
        market: bestMarket,
        price,
        isPreferred: habits[productName] === brand,
      }
    })
    .filter(Boolean)

  const total = products.reduce((sum, p) => sum + p.price, 0)

  const marketTotals = {}
  for (const p of products) {
    marketTotals[p.market] = (marketTotals[p.market] || 0) + p.price
  }
  const bestMarket =
    Object.entries(marketTotals).sort((a, b) => a[1] - b[1])[0]?.[0] ||
    'Migros'

  return { products, total, bestMarket }
}
